/**
 * Stores the movement direction of the player.
 */
public enum MoveDirection {
    /**
     * The player is moving to the left.
     */
    LEFT,

    /**
     * The player is not moving.
     */
    NONE,

    /**
     * The player is moving to the right.
     */
    RIGHT
}
